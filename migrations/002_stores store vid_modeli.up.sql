CREATE TABLE IF NOT EXISTS store (
 id BIGINT GENERATED BY DEFAULT AS IDENTITY  PRIMARY KEY,
 id_1c TEXT NOT NULL UNIQUE,
 name_1c TEXT NOT NULL,
 address TEXT,
 registrator_id INT NOT NULL REFERENCES registrator(id),
 link_2gis TEXT,
 phone TEXT,
 city TEXT,
 image_path TEXT,
 public BOOLEAN NOT NULL DEFAULT FALSE,
 working_hours JSONB,
 doublegis_widget_url TEXT,
 yandex_widget_url TEXT,
 store_kaspi_id TEXT,
 changed_date TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP, 
 create_date TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS vid_modeli (
 id BIGINT GENERATED BY DEFAULT AS IDENTITY  PRIMARY KEY,
 id_1c TEXT UNIQUE NOT NULL UNIQUE,
 name_1c TEXT NOT NULL,
 registrator_id BIGINT NOT NULL REFERENCES registrator(id),
 changed_date TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP, 
 create_date TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP
);